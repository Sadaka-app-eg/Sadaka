window.startAcademyQuizEngine = function(category, level) {
    const container = document.getElementById('quranQuizzesContainer');
    if (!container) return;

    const bank = window.quranAcademyQuizzes[category][level];
    
    // سحب عشوائي ذكي لـ 10 أسئلة فريدة ومختلفة تماماً في كل مرة يدخل فيها التحدي
    const shuffled = [...bank].sort(() => 0.5 - Math.random());
    const selectedQuestions = shuffled.slice(0, 10);

    window.currentActiveQuranQuiz = {
        category: category,
        level: level,
        questions: selectedQuestions,
        answers: new Array(10).fill(null)
    };

    window.renderAcademyQuizQuestionsHTML();
};

