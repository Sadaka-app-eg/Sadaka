window.startAcademyQuizEngine = function(category, level) {
    const container = document.getElementById('quranQuizzesContainer');
    if (!container) return;

    // جلب النماذج الثلاثة المتوفرة لهذا المستوى بالذات بالملي
    const allModels = window.quranAcademyQuizzes[category][level];
    
    // سحب نموذج عشوائي بالكامل من الـ 3 نماذج الجاهزة لضمان التجديد 100%
    const randomIndex = Math.floor(Math.random() * allModels.length);
    const selectedQuestions = allModels[randomIndex];

    window.currentActiveQuranQuiz = {
        category: category,
        level: level,
        modelNumber: randomIndex + 1, // تذكر رقم النموذج العشوائي
        questions: selectedQuestions,
        answers: new Array(10).fill(null)
    };

    window.renderAcademyQuizQuestionsHTML();
};

