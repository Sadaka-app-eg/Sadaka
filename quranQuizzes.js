window.shareAcademyQuizCertificateImage = function() {
    const certElement = document.getElementById('athrPrintableCert');
    if (!certElement || typeof html2canvas !== 'function') return;

    html2canvas(certElement, { scale: 2, backgroundColor: '#fdfbf7', width: 1120, height: 792 }).then(async canvas => {
        canvas.toBlob(async (blob) => {
            if (!blob) return;
            const file = new File([blob], 'athr_academy_certificate.png', { type: 'image/png' });
            
            if (navigator.canShare && navigator.share && navigator.canShare({ files: [file] })) {
                try {
                    await navigator.share({
                        files: [file],
                        title: 'بفضل الله حزت شهادة إتقان جديدة من أكاديمية أثر! ✨',
                        text: 'الحمد لله، قفّلت اختبار الطاعة والعلوم الشرعية بنجاح وعاينت وثيقتي المعتمدة الحين!'
                    });
                } catch (e) { console.log("Share skipped:", e); }
            } else {
                alert("💾 الحفظ المباشر مدعوم، يمكنك تحميل الصورة من الزر الملون ومشاركتها يدويّاً بروقان!");
                window.saveAcademyQuizCertificateAsImage();
            }
        }, 'image/png');
    });
};

