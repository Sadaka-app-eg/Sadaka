function renderAbwabTopicsNav() {
  const nav = document.getElementById('abwabTopicsNav');
  const topics = window._abwabCurrentTopics || [];
  if (!nav) return;
  nav.innerHTML = topics.map((t, i) => `
    <button class="prayer-step-btn ${i === (window._abwabCurrentIndex || 0) ? 'active' : ''}" onclick="showAbwabTopic(${i})">${t.title}</button>
  `).join('');
}

function showAbwabTopic(index) {
  window._abwabCurrentIndex = index;
  const topics = window._abwabCurrentTopics || [];
  const topic = topics[index];
  const container = document.getElementById('abwabTopicContent');
  if (!container || !topic) return;

  let html = `<div class="prayer-step-card">`;
  html += `<div class="prayer-step-title">${topic.title}</div>`;
  if (topic.body) {
    html += `<p style="font-size:14px; line-height:2; color:var(--text); font-family:'Amiri',serif; text-align:justify; white-space:pre-line;">${topic.body}</p>`;
  }

  if (topic.quran) {
    html += `<div class="prayer-section-label">📖 من القرآن الكريم</div>`;
    html += `<div class="prayer-quran-text">﴿ ${topic.quran} </div>`;
    if (topic.quranRef) html += `<div style="text-align:left; font-size:11px; color:var(--gold); margin-bottom:8px;">${topic.quranRef}</div>`;
  }

  if (topic.hadiths && topic.hadiths.length) {
    html += `<div class="prayer-section-label">📜 الدليل من السنة</div>`;
    html += topic.hadiths.map(h => `
      <div class="prayer-hadith-item">
        <div class="prayer-hadith-text">«${h.text}»</div>
        <div class="prayer-hadith-src">${h.src}</div>
      </div>
    `).join('');
  }

  if (topic.jumhur) {
    html += `<div class="prayer-section-label">👥 قول الجمهور</div>`;
    html += `<div class="prayer-jumhur-box">${topic.jumhur}</div>`;
  }

  if (topic.khilaf && topic.khilaf.length) {
    html += `<div class="prayer-section-label">⚖️ مسائل الخلاف بين أهل العلم</div>`;
    html += topic.khilaf.map(k => `
      <div class="prayer-khilaf-item">
        <div class="prayer-khilaf-opinion">${k.opinion}</div>
        <div class="prayer-khilaf-madhab">${k.madhab}</div>
        <div class="prayer-khilaf-evidence">${k.evidence}</div>
      </div>
    `).join('');
  }

  html += `<div class="prayer-nav-buttons">`;
  if (index > 0) html += `<button class="prayer-nav-btn" onclick="showAbwabTopic(${index - 1})">➡ السابق</button>`;
  if (index < topics.length - 1) html += `<button class="prayer-nav-btn" onclick="showAbwabTopic(${index + 1})">التالي ⬅</button>`;
  html += `</div>`;

  // خطة عملية — تظهر فقط في آخر موضوع بكل باب
  if (index === topics.length - 1) {
    const planKey = window._abwabCurrentPlanKey;
    const planItems = abwabElmPlans[planKey];
    if (planItems && planItems.length) {
      html += renderAbwabPlanBox(planKey, planItems);
    }
  }

  html += `</div>`;
  container.innerHTML = html;
  renderAbwabTopicsNav();
  container.scrollIntoView({ behavior: 'smooth', block: 'start' });
}

function renderAbwabPlanBox(planKey, items) {
  const state = JSON.parse(localStorage.getItem('abwab_plan_' + planKey) || '{}');
  const itemsHtml = items.map((item, i) => {
    const checked = state[i] ? 'checked' : '';
    return `
      <div class="abwab-plan-item">
        <div class="abwab-plan-check ${checked}" onclick="toggleAbwabPlanItem('${planKey}', ${i})">${state[i] ? '✓' : ''}</div>
        <div class="abwab-plan-text">${item}</div>
      </div>
    `;
  }).join('');

  return `
    <div class="abwab-plan-box">
      <div class="abwab-plan-title">📌 خطة عملية</div>
      ${itemsHtml}
    </div>
  `;
}

function toggleAbwabPlanItem(planKey, index) {
  let state = JSON.parse(localStorage.getItem('abwab_plan_' + planKey) || '{}');
  state[index] = !state[index];
  localStorage.setItem('abwab_plan_' + planKey, JSON.stringify(state));
  showAbwabTopic(window._abwabCurrentIndex);
  if (typeof boostFlame === 'function') boostFlame(1);
}
