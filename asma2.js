function renderAsma2(list) {
  const el = document.getElementById('asma2List');
  if (!el) return;
  el.innerHTML = list.map(a => `
    <div class="asma2-card" onclick="this.classList.toggle('expanded')">
      <div class="asma2-num">${toAr(a.n)}</div>
      <div class="asma2-name">${a.name}</div>
      <div class="asma2-meaning">${a.meaning}</div>
      <div class="asma2-fadl">✨ ${a.fadl}</div>
      <div class="asma2-details">
        <div style="color:var(--gold);font-size:12px;margin-bottom:6px;font-family:'Amiri',serif;">🤲 ادعُ الله بهذا الاسم:</div>
        <div class="asma2-doaa">${a.doaa}</div>
      </div>
    </div>
  `).join('');
}

function filterAsma2(q) {
  if(!q) { renderAsma2(asma2Allah); return; }
  renderAsma2(asma2Allah.filter(a =>
    a.name.includes(q) ||
    a.meaning.includes(q) ||
    a.n.toString() === q
  ));
}

document.addEventListener('DOMContentLoaded', () => {
  renderAsma2(asma2Allah);
});
